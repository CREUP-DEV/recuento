export type ApiErrorMessageKey = keyof (typeof apiErrorMessages)['es']

export function getDefaultApiErrorMessage(key: ApiErrorMessageKey) {
  return apiErrorMessages.es[key]
}

export function getApiErrorMessage(event: H3Event, key: ApiErrorMessageKey) {
  const language = getRequestHeader(event, 'accept-language')?.toLowerCase() ?? ''
  return language.startsWith('en') ? apiErrorMessages.en[key] : apiErrorMessages.es[key]
}
